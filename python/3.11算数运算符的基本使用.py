# python支持很多运算符,如：+、-、*、/、**幂运算、 //整除、 %取余
print(1 + 1)    #2
print(4 - 2)    #2
print(3 * 2)    #6

#python3中，两个整数相除，结果为浮点数
print(6 / 2)    #3.0
print(9 / 2)    #4.5
print(10 / 3)   #3.3333333333333335

print(3 ** 3)   #27
print(81 ** (1 / 2)) #9.0

print(10 // 3)  #3
print(-5 // 2)  #-3 向下整除

print(10 % 3)   #1 取余
print(1986 % 8412332)   #1986
